import re


def is_valid_IP(strng):
    strng = strng.replace(' ', '')
    pattern = r"^(([0-9]|[1-9][0-9]|1[0-9]{2}|2[0-4][0-9]|25[0-5])\.){3}([0-9]|[1-9][0-9]|1[0-9]{2}|2[0-4][0-9]|25[0-5])$"
    pattern2 = r"^([0-9]|[1-9][0-9]|1[0-9][0-9]|2[0-4][0-9]|25[0-5])\.([0-9]|[1-9][0-9]|1[0-9][0-9]|2[0-4][0-9]|25[0-5])\.([0-9]|[1-9][0-9]|1[0-9][0-9]|2[0-4][0-9]|25[0-5])\.([0-9]|[1-9][0-9]|1[0-9][0-9]|2[0-4][0-9]|25[0-5])$"
    pattern3 = r'(?:^|\b(?<!\.))(?:1?\d\d?|2[0-4]\d|25[0-5])(?:\.(?:1?\d\d?|2[0-4]\d|25[0-5])){3}(?=$|[^\w.])'

    if re.search(pattern=pattern3, string=strng):
        return True
    else:
        return False


print(is_valid_IP('12.255.56.1'))
print(is_valid_IP('12.255.56.1'))
print(is_valid_IP(''))
print(is_valid_IP('123.045.067.089'))
print(is_valid_IP('1.2.3.4'))
print(is_valid_IP(' 1.2.3.4'))
